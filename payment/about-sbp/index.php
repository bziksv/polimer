<p>«СБПэй» — это отдельное мобильное приложение Системы быстрых платежей, российский аналог Apple Pay и Google Pay. Оно доступно для Android (версия 7.0 и выше) и iOS, скачать его можно только в официальных магазинах RuStore и AppGallery.</p>
<p>После установки нужно привязать банковский счёт — деньги будут списываться напрямую, без ввода данных карты. Оплачивать покупки можно двумя способами:</p>
<ul>
	<li>По NFC: разблокировать смартфон и приложить к терминалу с отметкой бесконтактной оплаты.</li>
	<li>По QR‑коду: открыть приложение, навести камеру на QR‑код продавца и подтвердить платеж.</li>
</ul>
<p>Оба способа занимают всего несколько секунд.</p>
